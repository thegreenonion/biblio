    <h2 id="anmelden">Anmelden</h2>
    <p>Um sich anzumelden, klicken Sie auf den Link "Anmelden" auf der Hauptseite und geben Sie Ihre Benutzerdaten ein.</p>

    <h2 id="registrieren">Registrieren</h2>
    <p>Falls Sie noch keinen Account haben, können Sie sich registrieren, indem Sie auf "Registrieren als Nutzer" klicken und das Formular ausfüllen.</p>

    <h2 id="buch-suchen">Nach Buch suchen</h2>
    <p>Um nach einem Buch zu suchen, klicken Sie auf "Nach Buch suchen" und geben Sie die Suchkriterien ein.</p>

    <h2 id="buch-hinzufuegen">Buch hinzufügen</h2>
    <p>Nur Hilfskräfte der Bibliothek können neue Bücher hinzufügen, indem sie auf "Buch hinzufügen" klicken und die Buchdetails eingeben.</p>

    <h2 id="ausleihen">Ausleihen</h2>
    <p>Um ein Buch auszuleihen, klicken Sie auf "Ausleihen" und folgen Sie den Anweisungen.</p>

    <h2 id="alle-ausleihen">Alle Ausleihen anzeigen</h2>
    <p>Nur Hilfskräfte der Bibliothek können alle Ausleihen anzeigen, indem sie auf "Alle Ausleihen" klicken.</p>

    <h2 id="offene-ausleihen">Offene Ausleihen anzeigen</h2>
    <p>Nut Hilfskräfte der Bibliothek können offene Ausleihen anzeigen, indem sie auf "Offene Ausleihen" klicken.</p>
